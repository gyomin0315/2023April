
public class Operator_3 {
	
	
	
	public static void main(String[]args)
	{
		int a = 10;
		int b = 10;
		
		++a;
		b++;
		
		System.out.println("a : "+ a);
		System.out.println("b : "+ b);
		
		int c = 10;
		int d = ++c;
		
		System.out.println("d: " + d);
		
		int e = 10;
		int f = e++;
		
		System.out.println("f: " + f);
		

	}
	
}

//출력 결과
/*
a : 11
b : 11
d: 11
f: 10


*/

