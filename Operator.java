
public class Operator {
	
	
	
	public static void main(String[]args)
	{
		int number1 = 10;
		System.out.println("number1 = 10 ->"+ number1);
		
		number1 +=10; // = number1 = number1 + 10
		
		System.out.println("number1 = 10 ->"+ number1);
		
		number1 -=10;
		
		System.out.println("number1 = 10 ->"+ number1);
		
		number1 *=2;
		
		System.out.println("number1 = 10 ->"+ number1);
		
		number1 /=2;
		
		System.out.println("number1 = 10 ->"+ number1);
		
		number1 %= 3; // 나머지
		
		System.out.println("number1 = 10 ->"+ number1);
		
		

	}
	
}

